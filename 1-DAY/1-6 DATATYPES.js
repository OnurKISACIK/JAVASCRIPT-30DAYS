//**----------  INTRUDUCING DATE TYPES  ----------- */

//* JAVASCRIPT ANS OTHER PROGRAMING LANGUAGES THEY ARE DIFFERENT TYPE OF DATE TYPES
//* THE FOLLOWING ARE JAVA SCRIPT PRIMITIVE DATA TYPES

//* - STRING
//* - NUMBER
//* - BOOLEAN
//* - NULL
//* - UNDEFINED
//* - SYMBOL

//?  **** NUMBERS ****

//* INTEGERS: INTEGER (NEGATIVE,ZERO,POSITIVE) NUMBERS
//* ...., -3, -2, -1, 0, 1, 2, 3 ....

//* FLOATPOINT NUMBERS: DECIMAL NUMBERS
//* ..... -3.5,- 2.25, -1.10, 0.0, 1.1, 2.3, 3.6 ....

//?  **** STRINGS ****

//* A COLLECTION OF ONE OR MORE CHARACTERS BETWEEN
//* TWO SINGLE QUOTES, DOUBLE QUOTES, BACKTICKS.

//* "DAVID", 'KEVIN', `MALE`

//?  **** BOOLEANS ****

//* A BOOLEAN VALUE IS EITHER TRUE OR FALSE

//* TRUE // IF THE LIGHT IS ON
//* FALSE // IF THE LIGHT IS OFF

//?  **** UNDEFINED ****

//* IN JAVA SCRIPT IF WE DO NOT ASSIGN A VALUE TO A VARIABLE, THE VALUE IS UNDEFINED.

let firstName;
console.log(firstName);

//? UNDEFINED
//* (BECAUSE WE DID NOT ASSIGNED "firstName" VARIABLE ANYTHING)

//?  **** NULL ****

//* NULL MEANS EMPTY VALUE

let cars = null;
console.log(cars);

//? NULL (EMPTY)

//* -------  CHEKCING DATA TYPES  ------**/

//* TO CHECK DATA TYPE OF CERTAIN VARIABLE WE USE THE "typeof" operator.

console.log(typeof "Onur");
//? STRING

console.log(typeof 5);
//? NUMBER

console.log(typeof true);
//? BOOLEAN

console.log(typeof null);
//? NULL

console.log(typeof undefined);
//?UNDEFINED
