//*  ------  INCREMENT OPERATOR  -----  */

//* WE USE THE INCREMENT OPERATOR TO INCREASE A VALUE STORED IN A VARIABLE
//* THE INCREMEBT COULD BE PRE OR POST INCREMENT

//* - PRE INCREMENT

let count = 0;
console.log(++count);
console.log(count);
//? 1
//? 1

//* - POST INCREMENT

let count1 = 0;
console.log(count1++);
console.log(count1);
//? 0
//? 1

//*  ------  DECREMENT OPERATOR  -----  */

//* WE USE THE DECREMENT OPERATOR TO DECREASE A VALUE IN A VARIABLE

//* - PRE DECREMENT

let count2 = 0;
console.log(--count2);
console.log(count2);
//? -1
//? -1

//* - POST DECREMENT

let count3 = 0;
console.log(count3--);
console.log(count3);
//? 0
//? -1
